# Operators => +, - , *, /, %

# int vs float

print(100)
print(type(100))

print(1 + 5)  # 6
print(type(1 + 5))  # int
print(1 - 5)  # -4
print(type(1 - 5))  # int

print(2 * 3)  # 6
print(type(2 * 3))  # int

print(2 / 3)  # 0.6666
print(type(2 / 3))  # float

print(2 % 3)  # 2
print(3 % 2)  # 1
print(type(3 % 2))  # int

# power (2 ** 3) => 2 * 2 * 2
print(2 ** 3)  # 8

# RoundOff value
print(2 / 3)
print(2 // 3)
print(3 / 2)
print(3 // 2)


